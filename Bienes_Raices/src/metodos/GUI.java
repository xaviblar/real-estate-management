    private void comprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comprarActionPerformed
        if (np.primero!=null){
            if (nc.primero!=null){
            //codigo de compra
            int tipo=combo_opcion.getSelectedIndex();
            String cli=(String)combo_comprar.getSelectedItem();
            String propie=numero.getText();
            String tran=(String)combo_opcion.getSelectedItem();
            String fecha=fech.getText();
            String alq=mensualidad.getText();
            String vent=valor.getText();
            String garan=garantia.getText();
            //Comprar","Alquilar
            if (tipo==0){
                lva.InsertarVA(nc.BuscarC(cli), tran, np.BuscarA(Integer.parseInt(propie)), fecha, Integer.parseInt(vent));
                nc.BuscarC(cli).setTipo("activo");
                np.BorrarA(Integer.parseInt(propie));
                JOptionPane.showMessageDialog(null, "La compra de la propiedad ha sido exitosa");
             }
            else if(tipo==1){
                lva.InsertarVA(nc.BuscarC(cli), tran, np.BuscarA(Integer.parseInt(propie)), fecha, Integer.parseInt(alq));
                nc.BuscarC(cli).setTipo("activo");
                np.BorrarA(Integer.parseInt(propie));
                JOptionPane.showMessageDialog(null, "El alquiler de la propiedad ha sido exitosa");
                
                //-----------------------------------------combo de mantenimiento
                combo_pro.removeAllItems();
                ListaVentaAlquiler temp = lva.primero;
                while (temp != null) {
                    combo_pro.addItem(temp.getPropiedad().getNumeroFinca());
                    temp = temp.siguiente;
                }
                             
            }  
            if(np.getPrimero()==null){
            titulo.setText("Vacio");
            numero.setText("Vacio");
            valor.setText("Vacio");
            mensualidad.setText("Vacio");
            garantia.setText(("Vacio"));
            fotodelproducto.setIcon(null);
            derecha.setEnabled(false);
            izquierda.setEnabled(false);
            }
            else{insertar(np.getPrimero());}
            }
            else{JOptionPane.showMessageDialog(null, "No hay clientes registrados");}}
        else {JOptionPane.showMessageDialog(null, "No hay propiedades en este momento");}
    }//GEN-LAST:event_comprarActionPerformed

