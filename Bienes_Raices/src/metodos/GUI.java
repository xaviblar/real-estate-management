    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        String lcedula = l_cedula.getText();
        String ltelefono = l_telefono.getText();
        String lnombre = l_nombre.getText();
        String lemail = l_email.getText();
        String lestado = (String) combo_estado.getSelectedItem();
        
            if (!lcedula.equals("") && !ltelefono.equals("") && !lnombre.equals("") && !lemail.equals("")) {

            if (nc.BuscarC(lcedula) == null) {
                    nc.InsertarC(lcedula,ltelefono,lnombre,lemail,lestado,"inactivo");
                    JOptionPane.showMessageDialog(null, "Registro con exito"+"\n"+nc.primero.toString());
                    l_cedula.setText("");
                    l_telefono.setText("");
                    l_nombre.setText("");
                    l_email.setText("");
                    //-----------------------------------------------------------------------UPDATE COMBO
                combo_comprar.removeAllItems();
                Cliente temp = nc.getPrimero();
                while (temp != null) {
                    combo_comprar.addItem(temp.getNombre());
                    temp = temp.sig;
                }
                    
            } else {
                JOptionPane.showMessageDialog(null, "La cedula ya existe");
                l_cedula.setText("");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Rellene todo los espacios");
        }
        
    }//GEN-LAST:event_registrarActionPerformed

