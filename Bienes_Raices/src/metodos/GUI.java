    private void consultasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultasActionPerformed
       int consult=consultas.getSelectedIndex();
       
       if (consult==0){//alquiladas
           pr.removeAllItems();
            ListaVentaAlquiler temp1 = lva.primero;
            while (temp1 != null) {
                if(temp1.getTipoTransaccion().equals("Alquilar")){
                    pr.addItem(temp1.getPropiedad().getNumeroFinca());
                    temp1 = temp1.siguiente;
                }
                else{temp1 = temp1.siguiente;      }
            }
       }
       else if(consult==1){//vendidas
           pr.removeAllItems();
            ListaVentaAlquiler temp1 = lva.primero;
            while (temp1 != null) {
                if(temp1.getTipoTransaccion().equals("Comprar")){
                    pr.addItem(temp1.getPropiedad().getNumeroFinca());
                    temp1 = temp1.siguiente;
                }
                else{temp1 = temp1.siguiente;      }
            }
       }
       else if(consult==2){// cliente interezado
           pr.removeAllItems();
            Cliente temp1 = nc.primero;
            while (temp1 != null) {
                if(temp1.getTipo().equals("inactivo")){
                    pr.addItem(temp1.getNombre());
                    temp1 = temp1.sig;
                }
                else{temp1 = temp1.sig;      }
            }
       }
       else if(consult==3){//clientes
           pr.removeAllItems();
            Cliente temp1 = nc.primero;
            while (temp1 != null) {
                if(temp1.getTipo().equals("activo")){
                    pr.addItem(temp1.getNombre());
                    temp1 = temp1.sig;
                }
                else{temp1 = temp1.sig;      }
            }
       }
       else if(consult==4){// mantenimiento
           pr.removeAllItems();
            Mantenimiento temp1 = ma.primero;
            while (temp1 != null) {
                    pr.addItem(temp1.getPropiedad().getPropiedad().getNumeroFinca());
                    temp1 = temp1.siguiente;
            }
       }
       else if(consult==5){// pago de alquiler y mantenimiento
           pr.removeAllItems();
            Pagos temp1 = pa.primero;
            while (temp1 != null) {
                    pr.addItem(temp1.getPropiedad().getPropiedad().getNumeroFinca());
                    temp1 = temp1.siguiente;
            }
       }
       else if(consult==6){// contrato de venta
           pr.removeAllItems();
            ListaVentaAlquiler temp1 = lva.primero;
            while (temp1 != null) {
                if(temp1.getTipoTransaccion().equals("Comprar")){
                    pr.addItem(temp1.getPropiedad().getNumeroFinca());
                    temp1 = temp1.siguiente;
                }
                else{temp1 = temp1.siguiente;      }
            }
       }
       else if(consult==7){// contrato de alquiler
           pr.removeAllItems();
            ListaVentaAlquiler temp1 = lva.primero;
            while (temp1 != null) {
                if(temp1.getTipoTransaccion().equals("Alquilar")){
                    pr.addItem(temp1.getPropiedad().getNumeroFinca());
                    temp1 = temp1.siguiente;
                }
                else{temp1 = temp1.siguiente;      }
            }
       }
    }//GEN-LAST:event_consultasActionPerformed

