    private void izquierdaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_izquierdaActionPerformed
        Propiedades temp = np.getPrimero();
        Propiedades temp2 = np.getPrimero();
        //if (temp==null)insertar(temp);
        if ((temp.getNumeroFinca()+"").equals(numero.getText())) {
            while (temp.siguiente != null) {
                temp = temp.siguiente;
            }
            insertar(temp);
        } else {
            while (!(temp.siguiente.getNumeroFinca()+"").equals(numero.getText())) {
                temp = temp.siguiente;
            }
            insertar(temp);
        }
    }//GEN-LAST:event_izquierdaActionPerformed

