    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Registrar mantenimiento
        int pro=(int)combo_pro.getSelectedItem();
        String mag=(String)combo_magnitud.getSelectedItem();
        String man=(String)combo_mante.getSelectedItem();
        String res=responsable.getText();
        String inv=inversion.getText();
        if(lva.primero!=null){
            if(!res.equals("") && !inv.equals("")){
                ma.InsertarMA(lva.BuscarVA(pro), man, res, fech.getText(), Integer.parseInt(inv),mag);
                responsable.setText("");
                inversion.setText("");
                JOptionPane.showMessageDialog(null, "Mantenimiento realizado con exito");
            }
            else{JOptionPane.showMessageDialog(null, "Revise la informacion introducida");}
        }
        else{JOptionPane.showMessageDialog(null, "Aun no hay propiedades alquiladas");}
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

